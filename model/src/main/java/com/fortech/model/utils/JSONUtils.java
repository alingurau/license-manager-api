package com.fortech.model.utils;

import org.json.*;

public final class JSONUtils {

    private JSONUtils() {}

    public static boolean isJSONValid(String test) {
        try {
            new JSONObject(test);
        } catch (JSONException ex) {
            try {
                new JSONArray(test);
            } catch (JSONException ex1) {
                return false;
            }
        }
        return true;
    }
}